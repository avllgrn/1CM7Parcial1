#include <stdio.h>
int main(void){
    int a;
    int b;
    printf("Ingresa un valor para a ");
    scanf("%d",&a);
    printf("Ingresa un valor para b ");
    scanf("%d",&b);
    printf("%d\n",a+b);
    printf("%d\n",a-b);
    printf("%d\n",a*b);
    printf("%d\n",a/b);
    printf("%d\n",a%b);
    return 0;
}
